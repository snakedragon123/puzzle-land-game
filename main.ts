scene.onOverlapTile(SpriteKind.Player, assets.tile`myTile1`, function (sprite, location) {
    if (Wood >= 1) {
        game.showLongText("A broken bridge. Repair Cost:1 Wood", DialogLayout.Center)
        tiles.setTileAt(tiles.getTileLocation(16, 5), assets.tile`myTile7`)
        tiles.setTileAt(tiles.getTileLocation(17, 5), assets.tile`myTile7`)
        tiles.setTileAt(tiles.getTileLocation(18, 5), assets.tile`myTile7`)
        tiles.setTileAt(tiles.getTileLocation(19, 5), assets.tile`myTile7`)
        tiles.setTileAt(tiles.getTileLocation(20, 5), assets.tile`myTile7`)
        tiles.setTileAt(tiles.getTileLocation(21, 5), assets.tile`myTile7`)
        tiles.setWallAt(tiles.getTileLocation(16, 5), false)
        tiles.setWallAt(tiles.getTileLocation(17, 5), false)
        tiles.setWallAt(tiles.getTileLocation(18, 5), false)
        tiles.setWallAt(tiles.getTileLocation(19, 5), false)
        tiles.setWallAt(tiles.getTileLocation(20, 5), false)
        tiles.setWallAt(tiles.getTileLocation(21, 5), false)
    } else {
        game.showLongText("A broken bridge. Maybe you can repair it with something around here", DialogLayout.Center)
        Mushroom_Man.x += -3
    }
})
